package iot.empiaurhouse.hermes.Model;

public class PlaceDataModel {

    private String placename, bannerimg_url, abouttext, phone_number, email_address, website_url, placetype;

    public String getPlacename() {
        return placename;
    }

    public void setPlacename(String placename) {
        this.placename = placename;
    }

    public String getBannerimg_url() {
        return bannerimg_url;
    }

    public void setBannerimg_url(String bannerimg_url) {
        this.bannerimg_url = bannerimg_url;
    }


    public String getAbouttext() {
        return abouttext;
    }

    public void setAbouttext(String abouttext) {
        this.abouttext = abouttext;
    }


    public String getEmail_address() {
        return email_address;
    }

    public void setEmail_address(String email_address) {
        this.email_address = email_address;
    }


    public String getPhone_number() {
        return phone_number;
    }


    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }


    public String getWebsite_url() {
        return website_url;
    }

    public void setWebsite_url(String website_url) {
        this.website_url = website_url;
    }


    public String getPlacetype() {
        return placetype;
    }


    public void setPlacetype(String placetype) {
        this.placetype = placetype;
    }
}
